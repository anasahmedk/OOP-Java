package za.com.dvt.oop.polymorphism.lab1;

public final class MathOperation {

    public int add (final int a, final int b){
        return a + b;
    }
    public int add (final int a, final int b, final int c){
        return a + b + c;
    }
    public int add (final int a, final int b, final int c, final int d){
        return a + b + c + d;
    }
}
